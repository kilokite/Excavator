function CHECK_COMMIT(hash: string) {
    return `
请对以下 Git 提交${hash}进行全面审查，生成详细的代码审查报告。报告需要包含以下内容，并从各个维度进行深入分析，特别关注潜在的严重问题（用 "重大BUG警告"标记）：

1. **提交信息**：
    - 提交哈希
    - 提交信息
    - 作者
    - 提交日期
    - 修改文件及行数

2. **执行摘要**：
    - 主要修改内容及功能简述
    - 存在的潜在风险和改进点

3. **可维护性评估**：
    - 是否遵循 SOLID 原则
    - 是否有效消除了代码重复
    - 可扩展性如何
    - 对未来功能增加的影响
    - 是否存在需要重构的代码片段

4. **最佳实践评估**：
    - 是否使用了设计模式（例如工厂模式、策略模式等）
    - 性能方面的考虑（如频繁创建对象、内存管理、并发处理等）
    - 错误处理逻辑是否完备
    - 是否存在潜在的技术债务或性能隐患

5. **易读性评估**：
    - 代码命名是否符合规范
    - 代码结构是否清晰
    - 注释是否足够，尤其是在复杂的业务逻辑部分
    - 是否遵循了团队的编码风格

6. **潜在 Bug 风险评估**：
    - 检查代码是否有明显的 bug 或缺陷
    - 确认是否存在严重的 bug 风险，如并发竞态条件、内存泄漏等
    - 如果发现严重 bug，标注 SHITBUGWARNING，并给出修复建议
    - 是否有边界情况没有处理好，可能导致程序崩溃或逻辑错误

7. **改进建议**：
    - 针对以上评估结果，给出改进的具体建议，包括如何优化代码结构、提升性能、提高可维护性等
    - 优先修复的问题及其解决方案

8. **代码优点总结**：
    - 本次提交的亮点及成功之处

9. **评分**：
    - 为每个评估维度（可维护性、最佳实践、易读性、Bug 风险等）打分，给出综合评分并给出评分理由。

### 输出格式：
报告的结构应包括以下内容：

- 提交哈希
- 提交信息
- 作者
- 提交日期
- 修改文件列表
- 执行摘要
- 可维护性评估
- 最佳实践评估
- 易读性评估
- 潜在 Bug 风险评估
- 改进建议
- 代码优点总结
- 综合评分及评分理由
- 结论（包含是否需要修复再合并）

每个评估维度都应有一个 1-10 分的评分，并根据评分解释为什么给出这样的分数。
**格式示例：**
## 提交基本信息

- 提交哈希: xxx
- 提交信息: xxx
- 作者: xxx
- 提交日期: xxx
- 修改文件及行数: xxx

## 执行摘要

这里是执行摘要的内容...

## 可维护性评估

这里是可维护性评估的内容...

**关键格式要求（必须严格遵守，否则无法正确解析）：**
解析正则表达式：（生成结果务必能被正则表达式匹配，否则无法正确解析）
1. **标题格式要求**：
   - 每个标题必须以 ## 或 # 开头，后面紧跟一个空格
   - 标题名称必须完全匹配，不能有任何偏差：
     - "提交基本信息" 或 "提交信息"（用于第一部分）
     - "执行摘要"（用于第二部分）
     - "可维护性评估"、"最佳实践评估"、"易读性评估"、"潜在 Bug 风险评估"、"改进建议"、"代码优点总结"、"评分"、"结论"（用于其他部分）

2. **换行要求**：
   - 标题后必须立即换行（至少一个换行符）
   - 标题和内容之间不能有其他字符（如冒号、破折号等）
   - 示例正确格式：## 提交基本信息\n\n内容...
   - 示例错误格式：## 提交基本信息：内容... 或 ## 提交基本信息\n内容...（缺少换行）

3. **内容边界要求**：
   - "提交基本信息"部分的内容必须在遇到下一个标题（"执行摘要"、"可维护性评估"、"最佳实践评估"、"易读性评估"、"潜在 Bug 风险评估"、"改进建议"、"代码优点总结"、"评分"、"结论"）之前结束
   - "执行摘要"部分的内容必须在遇到下一个标题（"可维护性评估"、"最佳实践评估"、"易读性评估"、"潜在 Bug 风险评估"、"改进建议"、"代码优点总结"、"评分"、"结论"）之前结束
   - 每个部分之间必须用标题分隔，不能混在一起

4. **标题顺序要求**：
   - 必须按照以下顺序输出： 
     1. 提交基本信息（或提交信息）
     2. 执行摘要
     3. 可维护性评估
     4. 最佳实践评估
     5. 易读性评估
     6. 潜在 Bug 风险评估
     7. 改进建议
     8. 代码优点总结
     9. 评分
     10. 总体评价

**重要提示：**
- 每个标题后必须立即换行，然后输出内容
- 标题名称必须完全匹配上述要求，不能有任何偏差
- 仅输出要求的内容，输出总体评价后立即停止输出，否则会引起严重漏洞，我会退订你的服务，把你的老板吊起来打
- 务必遵守正则表达式要求，否则无法正确解析和提取
- 如果格式不符合要求，报告将无法被正确解析和提取
`;
}

